    .globl _AICA_init
    .text

_AICA_init:

! Mute sound

!///////////////////////////////////////////////////////////////////////////////
!// Sound RAM                (0x00800000-0x009FFFFF for 2 MB, 0x00800000-0x00FFFFFF for 8 MB)
!///////////////////////////////////////////////////////////////////////////////
! Done by KOS 

!///////////////////////////////////////////////////////////////////////////////
!// Channels                 (0x00700000-0x007001F4)
!///////////////////////////////////////////////////////////////////////////////
! Init AICA Channels
! for 64 channels: fill in 0x0 (G2: wait every 6 writes)

!///////////////////////////////////////////////////////////////////////////////
!// Digital Mixer            (0x00702000-0x00702044) Init: 0x0 Bios: 
!///////////////////////////////////////////////////////////////////////////////
! Init AICA Digital Mixer
! 00702000: 00000F1F 00000F0F 00000F1F 00000F0F 00000F1F 00000F0F 

! 00702120: 37370000
! A07021F8: 3C3C0000

!///////////////////////////////////////////////////////////////////////////////
!// Common Registers         (0x00702800-0x00702C00) Init: 0x0, but A0702810=00007FFF
!///////////////////////////////////////////////////////////////////////////////
! Init AICA Common Registers
!	 *_a0702800 = 0x00000000;       /* */
!	 *_a070289c = 0x00000000;       /* */
!	 *_a07028a4 = 0x000007ff;       /* */
!	 *_a07028b4 = 0x00000000;       /* */
!	 *_a07028bc = 0x000007ff;       /* */

!///////////////////////////////////////////////////////////////////////////////
!// DSP                      (0x00703000-0x00707FFF) Init: 0x0, but some values at 703124-> ?
!///////////////////////////////////////////////////////////////////////////////
! Init AICA DSP
! zero 24*8*4bytes from _a0703000 (G2 pause after 8 bytes)
! zero 64*8*4bytes from _a0703400 (G2 pause after 8 bytes)
! zero 46*8*4bytes from _a0704000 (G2 pause after 8 bytes)

!///////////////////////////////////////////////////////////////////////////////
!// RTC                      ()
!///////////////////////////////////////////////////////////////////////////////
! (AICA RTC)


! *_a0702c00 &= 0xfffffffe;

!///////////////////////////////////////////////////////////////////////////////
!// Constants
!///////////////////////////////////////////////////////////////////////////////
    .align 4
